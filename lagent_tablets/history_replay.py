def _ensure_proof_active_node(state: SupervisorState, tablet: TabletState) -> None:
    current = state.active_node or tablet.active_node
    if state.phase == "proof_formalization" and current and current in tablet.nodes and tablet.nodes[current].status == "closed":
        current = ""
    if not current:
        if state.phase == "proof_complete_style_cleanup":
            cleanup_nodes = [n for n, node in sorted(tablet.nodes.items()) if node.kind != "preamble"]
            current = cleanup_nodes[0] if cleanup_nodes else ""
        else:
            open_nodes = [n for n, node in sorted(tablet.nodes.items()) if node.status == "open" and node.kind != "preamble"]
            current = open_nodes[0] if open_nodes else ""
    state.active_node = current
    tablet.active_node = current


def _stage_inventory_has_retries(cycle_artifacts: _CycleArtifacts, stage: str) -> Dict[str, List[int]]:
    prefixes = STAGE_PREFIXES[stage]
    return {
        base: attempts
        for base, attempts in cycle_artifacts.repeated_bases().items()
        if any(base.startswith(prefix) for prefix in prefixes)
    }


def _compare_prompt(
    *,
    original_config: Config,
    replay_config: Config,
    historical: _ArtifactInfo,
    replay_prompt: str,
    cycle: int,
    stage: str,
) -> Optional[ReplayResult]:
    normalized_historical = _normalize_prompt(historical.prompt, original_config, replay_config)
    normalized_replay = _normalize_prompt(replay_prompt, original_config, replay_config)
    if normalized_replay == normalized_historical:
        return None
    return ReplayResult(
        status="diverged",
        reason="Prompt text differs from historical run.",
        cycle=cycle,
        stage=stage,
        artifact=historical.exact_id,
        historical_ref=f"cycle-{cycle}",
        historical_prompt=normalized_historical,
        replay_prompt=normalized_replay,
        diff=_prompt_diff(normalized_historical, normalized_replay),
    )


