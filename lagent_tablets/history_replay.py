def _theorem_worker_prompt(
    config: Config,
    state: SupervisorState,
    tablet: TabletState,
    policy: Policy,
    *,
    previous_outcome: Optional[Dict[str, Any]],
    cycle: int,
) -> Tuple[str, str]:
    nl_cache = NLCache(config.state_dir / "nl_cache.json")
    _prepare_theorem_stating_worker_state(config, state, tablet, policy, nl_cache=nl_cache)
    preflight_error = _theorem_stating_preflight_error(state)
    if preflight_error:
        return "", preflight_error
    target = state.theorem_soundness_target.strip()
    mode = _theorem_target_edit_mode(state)
    log_dir = config.state_dir / "logs" / f"cycle-{cycle:04d}"
    authorized_region = sorted(_theorem_target_scope(config.repo_path, target)) if target else []
    scoped_payload = _scoped_tablet_check_payload_path(log_dir) if target and mode == "restructure" else None
    repair_payload = _theorem_target_repair_scope_payload_path(log_dir) if target and mode == "repair" else None
    edit_payload = _theorem_target_edit_scope_payload_path(log_dir) if target and mode == "restructure" else None
    return (
        build_theorem_stating_prompt(
            config,
            state,
            tablet,
            policy,
            previous_outcome=previous_outcome,
            authorized_region=authorized_region,
            scoped_tablet_check_payload_path=scoped_payload,
            repair_scope_check_payload_path=repair_payload,
            edit_scope_check_payload_path=edit_payload,
        ),
        "",
    )


