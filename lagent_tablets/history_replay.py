def _aggregate_correspondence_results(
    repo: Path,
    corr_nodes: List[str],
    agents: List[Any],
) -> Optional[Dict[str, Any]]:
    if not corr_nodes:
        return None
    if len(agents) >= 2:
        agent_results: List[Dict[str, Any]] = []
        for i, agent in enumerate(agents):
            selected_index = getattr(agent, "selected_index", i)
            data = _load_correspondence_agent_result(repo, selected_index)
            if not isinstance(data, dict):
                return None
            agent_results.append({
                "agent": agent.label or f"{agent.provider}/{agent.model}",
                "index": selected_index,
                "ok": True,
                **data,
            })
        overalls = [r.get("overall", "ERROR") for r in agent_results]
        all_approve = all(o == "APPROVE" for o in overalls)
        all_reject = all(o == "REJECT" for o in overalls)
        if all_approve or all_reject:
            overall = "APPROVE" if all_approve else "REJECT"
            return {
                "check": "correspondence",
                "overall": overall,
                "summary": f"Unanimous {overall} from {len(agent_results)} agents",
                "node_names": list(corr_nodes),
                "agent_results": agent_results,
            }
        disagree_detail = ", ".join(
            f"{result.get('agent', '?')}: {result.get('overall', '?')}"
            for result in agent_results
        )
        return {
            "check": "correspondence",
            "overall": "DISAGREE",
            "summary": f"Agents disagree: {disagree_detail}. Reviewer must arbitrate.",
            "node_names": list(corr_nodes),
            "agent_results": agent_results,
        }
    data = _load_single_correspondence_result(repo)
    if not isinstance(data, dict):
        return None
    return {"check": "correspondence", "node_names": list(corr_nodes), **data}


def _aggregate_soundness_results(
    config: Config,
    tablet: TabletState,
    proof_nodes: List[str],
    agents: List[Any],
    *,
    disagree_bias: str,
) -> Optional[List[Dict[str, Any]]]:
    if not proof_nodes:
        return None
    if len(agents) < 2:
        data = _load_single_soundness_batch(config.repo_path)
        if not isinstance(data, dict):
            return None
        return [{"check": "nl_proof", "node_names": list(proof_nodes), **data}]

    check_nodes = [
        name for name in proof_nodes
        if name in tablet.nodes and tablet.nodes[name].kind != "preamble"
    ]
    check_nodes = _soundness_priority_order(config.repo_path, tablet, check_nodes)
    node_verdicts: List[Dict[str, Any]] = []
    structural_issues: List[str] = []
    for node_name in check_nodes:
        if tablet.nodes[node_name].status == "closed":
            continue
        node_results: List[Dict[str, Any]] = []
        for i, agent in enumerate(agents):
            selected_index = getattr(agent, "selected_index", i)
            existing = _load_existing_soundness_result(
                config,
                node_name=node_name,
                agent_index=selected_index,
                agent_label=agent.label or f"{agent.provider}/{agent.model}",
            )
            if existing is None:
                path = config.repo_path / f"nl_proof_{node_name}_{selected_index}.json"
                data = load_json(path, default=None) if path.exists() else None
                if not isinstance(data, dict):
                    return None
                existing = {
                    "agent": agent.label or f"{agent.provider}/{agent.model}",
                    "node": node_name,
                    "index": selected_index,
                    "ok": True,
                    **data,
                }
            node_results.append(existing)
        overalls = [result.get("overall", "ERROR") for result in node_results]
        soundness_decisions = [
            result.get("soundness", {}).get("decision", "?")
            if isinstance(result.get("soundness"), dict)
            else "?"
            for result in node_results
        ]
        has_structural = any(decision == "STRUCTURAL" for decision in soundness_decisions)
        all_approve = all(overall == "APPROVE" for overall in overalls)
        all_reject = all(overall == "REJECT" for overall in overalls)
        panel_split = len(node_results) == 2 and len(set(overalls)) > 1
        if all_approve:
            node_overall = "APPROVE"
        elif all_reject:
            node_overall = "REJECT"
        elif panel_split:
            node_overall = "APPROVE" if disagree_bias == "approve" else "REJECT"
        else:
            node_overall = "REJECT"
        verdict: Dict[str, Any] = {
            "node": node_name,
            "agent_results": node_results,
            "overall": node_overall,
        }
        if panel_split:
            verdict["panel_split"] = True
            verdict["disagree_bias"] = disagree_bias
        if has_structural:
            verdict["structural"] = True
            structural_issues.append(node_name)
        node_verdicts.append(verdict)

    all_approve = all(verdict["overall"] == "APPROVE" for verdict in node_verdicts)
    summary_parts: List[str] = []
    if structural_issues:
        summary_parts.append(f"STRUCTURAL issues in: {structural_issues}")
    failed_nodes = [verdict["node"] for verdict in node_verdicts if verdict["overall"] != "APPROVE"]
    if failed_nodes:
        summary_parts.append(f"Failed: {failed_nodes}")
    return [{
        "check": "nl_proof",
        "overall": "APPROVE" if all_approve else "REJECT",
        "summary": "; ".join(summary_parts) if summary_parts else f"All {len(node_verdicts)} nodes sound",
        "structural_issues": structural_issues,
        "node_names": [verdict["node"] for verdict in node_verdicts],
        "node_verdicts": node_verdicts,
    }]


def _plan_verification(
    config: Config,
    state: SupervisorState,
    tablet: TabletState,
    policy: Policy,
    *,
    cycle: int,
    worker_ref: str,
    verification_ref: str,
) -> Tuple[List[PromptExpectation], List[Dict[str, Any]], Optional[ReplayResult]]:
    log_dir = config.state_dir / "logs" / f"cycle-{cycle:04d}"
    expectations: List[PromptExpectation] = []
    results: List[Dict[str, Any]] = []
    nl_cache = NLCache(config.state_dir / "nl_cache.json")

    corr_nodes_base: List[str] = []
    proof_nodes_base: List[str] = []
    persisted_correspondence_blocked = False
    force_correspondence_node_names: List[str] = []
    soundness_target_node: Optional[str] = None

    if state.phase == "theorem_stating":
        _backfill_legacy_correspondence_hashes(tablet, config.repo_path)
        _repair_stale_legacy_correspondence_failures(tablet, state, config.repo_path)
        corr_nodes_base = _theorem_stating_correspondence_frontier(
            tablet,
            config.repo_path,
            forced_nodes=_theorem_stating_persisted_correspondence_nodes(tablet, state),
        )
        proof_nodes_base = [
            name for name in tablet.nodes
            if name != PREAMBLE_NAME and tablet.nodes[name].kind != "preamble"
        ]
        persisted_correspondence_blocked = _theorem_stating_has_correspondence_blockers(state)
        force_correspondence_node_names = _theorem_stating_persisted_correspondence_nodes(tablet, state)
        soundness_target_node = state.theorem_soundness_target or None
    elif state.phase == "proof_formalization":
        worker_meta = _read_cycle_meta(config.repo_path, worker_ref)
        outcome = str(worker_meta.get("outcome", "") or "").strip().upper()
        if outcome == "PROGRESS":
            previous_ref = _root_commit(config.repo_path) if cycle == 1 else f"cycle-{cycle - 1}"
            if cycle > 1 and not _ref_exists(config.repo_path, previous_ref):
                return expectations, results, ReplayResult(
                    status="unreplayable",
                    reason=f"Missing previous final ref {previous_ref} while planning proof verification.",
                    cycle=cycle,
                    stage=VERIFICATION_STAGE,
                )
            previous_names: set[str] = set()
            if cycle > 1:
                _checkout_ref(config.repo_path, previous_ref)
                previous_tablet = load_tablet(tablet_path(config))
                previous_names = {
                    name for name, node in previous_tablet.nodes.items()
                    if node.kind != "preamble"
                }
                _checkout_ref(config.repo_path, worker_ref)
                state = load_state(state_path(config))
                tablet = load_tablet(tablet_path(config))
                _prepare_runtime_state(config, state, tablet, cycle=cycle)
            current_names = {
                name for name, node in tablet.nodes.items()
                if node.kind != "preamble"
            }
            created_nodes = sorted(current_names - previous_names)
            changed_nodes = sorted(
                {
                    name for name in created_nodes
                }
                | {
                    Path(path).stem
                    for path in _git(config.repo_path, "diff", "--name-only", previous_ref, worker_ref, "--", "Tablet/", check=False).stdout.splitlines()
                    if path.endswith(".lean") or path.endswith(".tex")
                }
            )
            verification_region = _proof_verification_region(config.repo_path, changed_nodes)
            corr_nodes_base = _proof_correspondence_frontier(
                tablet,
                config.repo_path,
                candidate_names=verification_region,
            )
            proof_nodes_base = _proof_soundness_frontier(
                tablet,
                config.repo_path,
                candidate_names=verification_region,
            )
        else:
            corr_nodes_base = []
            proof_nodes_base = []
    else:
        return expectations, results, None

    corr_nodes = list(corr_nodes_base)
    cached_corr_nodes: List[str] = []
    forced_corr = {str(name) for name in force_correspondence_node_names if str(name).strip()}
    if corr_nodes_base:
        uncached = set(nl_cache.filter_uncached(config.repo_path, corr_nodes_base, "correspondence"))
        corr_nodes = [name for name in corr_nodes_base if name in uncached or name in forced_corr]
        cached_corr_nodes = [name for name in corr_nodes_base if name not in set(corr_nodes)]
    if cached_corr_nodes:
        results.append({
            "check": "correspondence",
            "overall": "APPROVE",
            "summary": "cached",
            "node_names": cached_corr_nodes,
        })
    if corr_nodes:
        corr_agents = _effective_correspondence_agents(config, policy)
        if len(corr_agents) >= 2:
            for i, agent in enumerate(corr_agents):
                selected_index = getattr(agent, "selected_index", i)
                prompt = build_correspondence_prompt(
                    config,
                    tablet,
                    node_names=corr_nodes,
                    paper_tex="",
                    human_input=state.human_input,
                    output_file=f"correspondence_result_{selected_index}.json",
                    previous_results=[
                        _load_agent_previous_result(config.repo_path, selected_index)
                    ] if _load_agent_previous_result(config.repo_path, selected_index) else None,
                )
                expectations.append(
                    PromptExpectation(
                        stage=VERIFICATION_STAGE,
                        artifact_base=f"correspondence_result_{selected_index}",
                        prompt=prompt,
                    )
                )
            _checkout_ref(config.repo_path, verification_ref)
            results.append(_aggregate_correspondence_results(config.repo_path, corr_nodes, corr_agents) or {
                "check": "correspondence",
                "overall": "ERROR",
                "summary": "Missing correspondence artifacts in historical verification checkpoint.",
                "node_names": corr_nodes,
            })
            _checkout_ref(config.repo_path, worker_ref)
        else:
            prompt = build_correspondence_prompt(
                config,
                tablet,
                node_names=corr_nodes,
                paper_tex="",
                human_input=state.human_input,
            )
            expectations.append(
                PromptExpectation(
                    stage=VERIFICATION_STAGE,
                    artifact_base="correspondence_result",
                    prompt=prompt,
                )
            )
            _checkout_ref(config.repo_path, verification_ref)
            data = _load_single_correspondence_result(config.repo_path)
            results.append(
                {"check": "correspondence", "node_names": corr_nodes, **data}
                if isinstance(data, dict)
                else {
                    "check": "correspondence",
                    "overall": "ERROR",
                    "summary": "Missing correspondence_result.json in historical verification checkpoint.",
                    "node_names": corr_nodes,
                }
            )
            _checkout_ref(config.repo_path, worker_ref)

    corr_overall = "APPROVE"
    saw_corr = False
    for result in results:
        if result.get("check") == "correspondence":
            saw_corr = True
            corr_overall = str(result.get("overall", "?") or "?")
    if persisted_correspondence_blocked and not saw_corr:
        corr_overall = "REJECT"
        results.append({
            "check": "correspondence",
            "overall": "REJECT",
            "summary": (
                "Persisted correspondence/paper-faithfulness blockers remain open from a previous cycle; "
                "soundness stays gated until they are explicitly rechecked and cleared."
            ),
            "node_names": [],
        })
    if corr_overall != "APPROVE":
        return expectations, results, None

    proof_nodes = list(proof_nodes_base)
    if proof_nodes_base:
        proof_nodes = nl_cache.filter_uncached(config.repo_path, proof_nodes_base, "soundness")
    if proof_nodes and soundness_target_node is not None:
        if soundness_target_node in proof_nodes:
            proof_nodes = [soundness_target_node]
        else:
            proof_nodes = _soundness_priority_order(config.repo_path, tablet, proof_nodes)[:1]
    if not proof_nodes and proof_nodes_base:
        results.append({
            "check": "nl_proof",
            "overall": "APPROVE",
            "summary": "cached",
            "node_names": [],
        })
        return expectations, results, None
    if not proof_nodes:
        return expectations, results, None

    soundness_agents = _effective_soundness_agents(config, policy)
    if len(soundness_agents) >= 2:
        ordered_nodes = _soundness_priority_order(config.repo_path, tablet, proof_nodes)
        for node_name in ordered_nodes:
            if node_name not in tablet.nodes or tablet.nodes[node_name].status == "closed":
                continue
            for i, agent in enumerate(soundness_agents):
                selected_index = getattr(agent, "selected_index", i)
                prompt = build_node_soundness_prompt(
                    config,
                    tablet,
                    node_name=node_name,
                    paper_tex="",
                    human_input=state.human_input,
                    output_file=f"nl_proof_{node_name}_{selected_index}.json",
                    previous_issues=_load_previous_soundness_issues(
                        config.repo_path,
                        node_name=node_name,
                        agent_index=selected_index,
                    ),
                )
                expectations.append(
                    PromptExpectation(
                        stage=VERIFICATION_STAGE,
                        artifact_base=f"nl_proof_{node_name}_{selected_index}",
                        prompt=prompt,
                    )
                )
        _checkout_ref(config.repo_path, verification_ref)
        aggregated = _aggregate_soundness_results(
            config,
            tablet,
            proof_nodes,
            soundness_agents,
            disagree_bias=policy.verification.soundness_disagree_bias,
        )
        if aggregated is None:
            aggregated = [{
                "check": "nl_proof",
                "overall": "ERROR",
                "summary": "Missing soundness artifacts in historical verification checkpoint.",
                "node_names": list(proof_nodes),
            }]
        results.extend(aggregated)
        _checkout_ref(config.repo_path, worker_ref)
    else:
        prompt = build_nl_proof_prompt(
            config,
            tablet,
            node_names=proof_nodes,
            paper_tex="",
            human_input=state.human_input,
            output_file="nl_proof_result.json",
        )
        expectations.append(
            PromptExpectation(
                stage=VERIFICATION_STAGE,
                artifact_base="nl_proof_result",
                prompt=prompt,
            )
        )
        _checkout_ref(config.repo_path, verification_ref)
        data = _load_single_soundness_batch(config.repo_path)
        results.append(
            {"check": "nl_proof", "node_names": proof_nodes, **data}
            if isinstance(data, dict)
            else {
                "check": "nl_proof",
                "overall": "ERROR",
                "summary": "Missing nl_proof_result.json in historical verification checkpoint.",
                "node_names": list(proof_nodes),
            }
        )
        _checkout_ref(config.repo_path, worker_ref)

    return expectations, results, None


def _worker_output_for_cycle(repo_path: Path, cycle: int) -> str:
    artifacts = _load_cycle_artifacts(repo_path, cycle)
    worker = _first_attempt_artifact(artifacts, "worker_handoff")
    if worker is None:
        return ""
    return _read_output_log(repo_path, cycle, worker.exact_id)


def _reviewer_prompt(
    config: Config,
    state: SupervisorState,
    tablet: TabletState,
    policy: Policy,
    *,
    historical_repo: Path,
    cycle: int,
    historical_results: List[Dict[str, Any]],
    cycle_meta: Dict[str, Any],
) -> Tuple[bool, str]:
    worker_handoff = load_json(config.repo_path / "worker_handoff.json", default=None)
    worker_output = _worker_output_for_cycle(historical_repo, cycle)
    if state.phase == "theorem_stating":
        reviewer_state = _suspend_theorem_soundness_target(state) if any(
            str(result.get("check", "")).strip() == "correspondence" and str(result.get("overall", "")).strip().upper() != "APPROVE"
            for result in historical_results
        ) else state
        prompt = build_theorem_stating_reviewer_prompt(
            config,
            reviewer_state,
            tablet,
            policy,
            worker_handoff=worker_handoff if isinstance(worker_handoff, dict) else None,
            worker_output=worker_output[-15000:] if worker_output else "",
            nl_verification=historical_results if historical_results else None,
            main_result_issues=[],
            unsupported_nodes=[],
            validation_summary={
                "outcome": cycle_meta.get("outcome", ""),
                "detail": cycle_meta.get("detail", ""),
                "consecutive_invalids": (
                    state.validation_summary.get("consecutive_invalids", 0)
                    if isinstance(state.validation_summary, dict)
                    else 0
                ),
            },
            available_reset_checkpoints=[],
        )
        return True, prompt

    outcome = str(cycle_meta.get("outcome", "") or "").strip().upper()
    needs_nl_review = bool(historical_results)
    consecutive_invalids = (
        state.validation_summary.get("consecutive_invalids", 0)
        if isinstance(state.validation_summary, dict)
        else 0
    )
    invalid_escalation_threshold = 3
    needs_reviewer = (
        outcome in ("PROGRESS", "NO_PROGRESS")
        or needs_nl_review
        or outcome == "REJECTED"
        or (outcome == "INVALID" and consecutive_invalids >= invalid_escalation_threshold)
        or state.phase == "proof_complete_style_cleanup"
    )
    if not needs_reviewer:
        return False, ""
    prompt = build_reviewer_prompt(
        config,
        state,
        tablet,
        policy,
        worker_handoff=worker_handoff if isinstance(worker_handoff, dict) else None,
        worker_output=worker_output[-20000:] if worker_output else "",
        validation_summary={
            "outcome": outcome,
            "detail": cycle_meta.get("detail", ""),
            "consecutive_invalids": consecutive_invalids,
        },
        nl_verification=historical_results if historical_results else None,
    )
    return True, prompt


def _compare_expected_stage_prompts(
    *,
    original_config: Config,
    replay_config: Config,
    cycle: int,
    stage: str,
    historical_artifacts: _CycleArtifacts,
    expected: Sequence[PromptExpectation],
) -> Optional[ReplayResult]:
    repeated = _stage_inventory_has_retries(historical_artifacts, stage)
    if repeated:
        repeated_text = ", ".join(
            f"{base}: attempts {attempts}" for base, attempts in sorted(repeated.items())
        )
        return ReplayResult(
            status="unreplayable",
            reason=f"Historical {stage} stage used repeated attempts that were not checkpointed: {repeated_text}",
            cycle=cycle,
            stage=stage,
        )

    historical_stage = historical_artifacts.stage_artifacts(stage)
    expected_map = {item.artifact_base: item for item in expected}
    historical_bases = set(historical_stage.keys())
    expected_bases = set(expected_map.keys())
    if historical_bases != expected_bases:
        return ReplayResult(
            status="diverged",
            reason=(
                f"{stage.capitalize()} prompt set differs. "
                f"historical={sorted(historical_bases)} replay={sorted(expected_bases)}"
            ),
            cycle=cycle,
            stage=stage,
        )

    for base in sorted(expected_bases):
        historical = historical_stage[base]
        divergence = _compare_prompt(
            original_config=original_config,
            replay_config=replay_config,
            historical=historical,
            replay_prompt=expected_map[base].prompt,
            cycle=cycle,
            stage=stage,
        )
        if divergence is not None:
            return divergence
    return None


def find_first_history_divergence(
    config: Config,
    policy: Policy,
    *,
    max_cycle: Optional[int] = None,
) -> ReplayResult:
    history = get_cycle_history(config.repo_path)
    if max_cycle is not None:
        history = [entry for entry in history if int(entry.get("cycle", 0) or 0) <= max_cycle]
    if not history:
        return ReplayResult(status="match", reason="No committed cycle history found.")

    original_repo = config.repo_path.resolve()
    replay_repo = _clone_for_analysis(original_repo)
    replay_config = _remap_config(config, replay_repo)

    try:
        root = _root_commit(replay_repo)
        if not root:
            return ReplayResult(status="error", reason="Could not resolve initial commit in replay clone.")

        for entry in history:
            cycle = int(entry.get("cycle", 0) or 0)
            historical_artifacts = _load_cycle_artifacts(config.repo_path, cycle)

            previous_outcome = _current_previous_outcome(history, cycle)
            previous_invalid = previous_outcome and str(previous_outcome.get("outcome", "")).strip().upper() == "INVALID"

            pre_ref = root if cycle == 1 else f"cycle-{cycle - 1}"
            if cycle > 1 and not _ref_exists(config.repo_path, pre_ref):
                return ReplayResult(
                    status="unreplayable",
                    reason=f"Missing historical ref {pre_ref}.",
                    cycle=cycle,
                    stage=WORKER_STAGE,
                )

            _checkout_ref(replay_repo, pre_ref)
            state, tablet = _current_phase_for_cycle(replay_repo, config.workflow.start_phase)
            _prepare_runtime_state(replay_config, state, tablet, cycle=max(cycle - 1, state.cycle))

            if state.phase == "theorem_stating":
                prompt, preflight_error = _theorem_worker_prompt(
                    replay_config,
                    state,
                    tablet,
                    policy,
                    previous_outcome=previous_outcome,
                    cycle=cycle,
                )
                if preflight_error:
                    return ReplayResult(
                        status="diverged",
                        reason=f"Current theorem-stating preflight would stop before prompting: {preflight_error}",
                        cycle=cycle,
                        stage=WORKER_STAGE,
                    )
            else:
                prompt = _proof_worker_prompt(
                    replay_config,
                    state,
                    tablet,
                    policy,
                    previous_outcome=previous_outcome,
                    cycle=cycle,
                )

            worker_artifact = _first_attempt_artifact(historical_artifacts, "worker_handoff")
            if worker_artifact is None:
                return ReplayResult(
                    status="unreplayable",
                    reason="Historical worker prompt not found.",
                    cycle=cycle,
                    stage=WORKER_STAGE,
                )
            worker_divergence = _compare_prompt(
                original_config=config,
                replay_config=replay_config,
                historical=worker_artifact,
                replay_prompt=prompt,
                cycle=cycle,
                stage=WORKER_STAGE,
            )
            if worker_divergence is not None:
                if previous_invalid:
                    worker_divergence.status = "unreplayable"
                    worker_divergence.reason = (
                        "Previous committed cycle was INVALID, and exact worker prompt replay "
                        "cannot recover the historical build-output context from git history."
                    )
                return worker_divergence

            worker_retries = _stage_inventory_has_retries(historical_artifacts, WORKER_STAGE)
            if worker_retries:
                repeated_text = ", ".join(
                    f"{base}: attempts {attempts}" for base, attempts in sorted(worker_retries.items())
                )
                return ReplayResult(
                    status="unreplayable",
                    reason=f"Historical worker stage used repeated attempts that were not checkpointed: {repeated_text}",
                    cycle=cycle,
                    stage=WORKER_STAGE,
                )

            worker_ref = f"cycle-{cycle}-worker"
            if not _ref_exists(config.repo_path, worker_ref):
                return ReplayResult(
                    status="unreplayable",
                    reason=f"Missing historical worker checkpoint {worker_ref}.",
                    cycle=cycle,
                    stage=VERIFICATION_STAGE,
                )

            verification_ref = f"cycle-{cycle}-verification"
            if not _ref_exists(config.repo_path, verification_ref):
                return ReplayResult(
                    status="unreplayable",
                    reason=f"Missing historical verification checkpoint {verification_ref}.",
                    cycle=cycle,
                    stage=VERIFICATION_STAGE,
                )

            _checkout_ref(replay_repo, worker_ref)
            state = load_state(state_path(replay_config))
            tablet = load_tablet(tablet_path(replay_config))
            _prepare_runtime_state(replay_config, state, tablet, cycle=cycle)

            expected_verification, verification_results, planning_issue = _plan_verification(
                replay_config,
                state,
                tablet,
                policy,
                cycle=cycle,
                worker_ref=worker_ref,
                verification_ref=verification_ref,
            )
            if planning_issue is not None:
                return planning_issue
            verification_divergence = _compare_expected_stage_prompts(
                original_config=config,
                replay_config=replay_config,
                cycle=cycle,
                stage=VERIFICATION_STAGE,
                historical_artifacts=historical_artifacts,
                expected=expected_verification,
            )
            if verification_divergence is not None:
                return verification_divergence

            _checkout_ref(replay_repo, verification_ref)
            state = load_state(state_path(replay_config))
            tablet = load_tablet(tablet_path(replay_config))
            _prepare_runtime_state(replay_config, state, tablet, cycle=cycle)
            reviewer_needed, reviewer_prompt = _reviewer_prompt(
                replay_config,
                state,
                tablet,
                policy,
                historical_repo=config.repo_path,
                cycle=cycle,
                historical_results=verification_results,
                cycle_meta=_read_cycle_meta(config.repo_path, verification_ref),
            )
            expected_reviewer = (
                [PromptExpectation(stage=REVIEWER_STAGE, artifact_base="reviewer_decision", prompt=reviewer_prompt)]
                if reviewer_needed
                else []
            )
            reviewer_divergence = _compare_expected_stage_prompts(
                original_config=config,
                replay_config=replay_config,
                cycle=cycle,
                stage=REVIEWER_STAGE,
                historical_artifacts=historical_artifacts,
                expected=expected_reviewer,
            )
            if reviewer_divergence is not None:
                return reviewer_divergence

        return ReplayResult(status="match", reason="Current code matches recorded prompts through the checked committed history.")
    finally:
        shutil.rmtree(replay_repo.parent, ignore_errors=True)
