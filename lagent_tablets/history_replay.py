def _load_tag_json(repo: Path, ref: str, rel_path: str) -> Any:
    result = _git(repo, "show", f"{ref}:{rel_path}", check=False)
    if result.returncode != 0:
        return None
    try:
        return json.loads(result.stdout)
    except json.JSONDecodeError:
        return None


