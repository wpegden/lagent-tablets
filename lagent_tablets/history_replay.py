ARTIFACT_ATTEMPT_RE = re.compile(r"^(?P<base>.+?)(?:_attempt_(?P<attempt>\d{4}))?$")
WORKER_STAGE = "worker"
VERIFICATION_STAGE = "verification"
REVIEWER_STAGE = "reviewer"
STAGE_PREFIXES = {
    WORKER_STAGE: ("worker_handoff",),
    VERIFICATION_STAGE: ("correspondence_result", "nl_proof"),
    REVIEWER_STAGE: ("reviewer_decision",),
}


@dataclass
class PromptExpectation:
    stage: str
    artifact_base: str
    prompt: str


@dataclass
class ReplayResult:
    status: str  # match | diverged | unreplayable | error
    reason: str
    cycle: Optional[int] = None
    stage: str = ""
    artifact: str = ""
    historical_ref: str = ""
    historical_prompt: str = ""
    replay_prompt: str = ""
    diff: str = ""

    def to_dict(self) -> Dict[str, Any]:
        return asdict(self)


@dataclass
class _ArtifactInfo:
    exact_id: str
    base: str
    attempt: int
    prompt: str


@dataclass
class _CycleArtifacts:
    by_exact: Dict[str, _ArtifactInfo]
    by_base: Dict[str, List[_ArtifactInfo]]

    def repeated_bases(self) -> Dict[str, List[int]]:
        repeated: Dict[str, List[int]] = {}
        for base, items in self.by_base.items():
            attempts = sorted({item.attempt for item in items})
            if len(attempts) > 1 or any(attempt > 1 for attempt in attempts):
                repeated[base] = attempts
        return repeated

    def stage_artifacts(self, stage: str) -> Dict[str, _ArtifactInfo]:
        prefixes = STAGE_PREFIXES[stage]
        selected: Dict[str, _ArtifactInfo] = {}
        for base, items in self.by_base.items():
            if not any(base.startswith(prefix) for prefix in prefixes):
                continue
            preferred = None
            for item in items:
                if item.attempt == 1:
                    preferred = item
                    break
            if preferred is None and len(items) == 1:
                preferred = items[0]
            if preferred is not None:
                selected[base] = preferred
        return selected


def _git(repo: Path, *args: str, check: bool = True) -> subprocess.CompletedProcess:
    return subprocess.run(
        ["git", *args],
        cwd=str(repo),
        capture_output=True,
        text=True,
        check=check,
    )


def _root_commit(repo: Path) -> Optional[str]:
    result = _git(repo, "rev-list", "--max-parents=0", "HEAD", check=False)
    if result.returncode != 0:
        return None
    lines = [line.strip() for line in result.stdout.splitlines() if line.strip()]
    return lines[0] if lines else None


def _ref_exists(repo: Path, ref: str) -> bool:
    return _git(repo, "rev-parse", "--verify", ref, check=False).returncode == 0


def _clone_for_analysis(original_repo: Path) -> Path:
    tmp_root = Path(tempfile.mkdtemp(prefix="lagent-history-replay-"))
    clone_path = tmp_root / "repo"
    subprocess.run(
        ["git", "clone", "--quiet", str(original_repo), str(clone_path)],
        check=True,
        capture_output=True,
        text=True,
    )
    lake_dir = original_repo / ".lake"
    if lake_dir.exists():
        shutil.copytree(lake_dir, clone_path / ".lake", dirs_exist_ok=True)
    return clone_path


def _checkout_ref(repo: Path, ref: str) -> None:
    subprocess.run(
        ["git", "checkout", "--force", "--quiet", ref],
        cwd=str(repo),
        check=True,
        capture_output=True,
        text=True,
    )


def _remap_repo_local_path(path: Optional[Path], old_repo: Path, new_repo: Path) -> Optional[Path]:
    if path is None:
        return None
    resolved = path.resolve()
    try:
        rel = resolved.relative_to(old_repo.resolve())
    except ValueError:
        return resolved
    return (new_repo / rel).resolve()


def _remap_config(config: Config, replay_repo: Path) -> Config:
    remapped = copy.deepcopy(config)
    remapped.repo_path = replay_repo.resolve()
    remapped.goal_file = _remap_repo_local_path(config.goal_file, config.repo_path, replay_repo) or config.goal_file
    remapped.state_dir = _remap_repo_local_path(config.state_dir, config.repo_path, replay_repo) or config.state_dir
    remapped.workflow.paper_tex_path = _remap_repo_local_path(
        config.workflow.paper_tex_path, config.repo_path, replay_repo
    )
    remapped.workflow.approved_axioms_path = (
        _remap_repo_local_path(config.workflow.approved_axioms_path, config.repo_path, replay_repo)
        or config.workflow.approved_axioms_path
    )
    remapped.workflow.human_input_path = (
        _remap_repo_local_path(config.workflow.human_input_path, config.repo_path, replay_repo)
        or config.workflow.human_input_path
    )
    remapped.workflow.input_request_path = (
        _remap_repo_local_path(config.workflow.input_request_path, config.repo_path, replay_repo)
        or config.workflow.input_request_path
    )
    remapped.chat.root_dir = remapped.state_dir / "chats"
    return remapped


def _normalize_prompt(prompt: str, original: Config, replay: Config) -> str:
    text = str(prompt or "")
    replacements: List[Tuple[str, str]] = []
    source_skills_dir = str((Path(__file__).resolve().parent.parent / "skills").resolve())
    for old, new in [
        (str(replay.state_dir), str(original.state_dir)),
        (str(replay.repo_path), str(original.repo_path)),
        (str(replay.goal_file), str(original.goal_file)),
        (str(replay.workflow.paper_tex_path or ""), str(original.workflow.paper_tex_path or "")),
        (str(replay.workflow.approved_axioms_path), str(original.workflow.approved_axioms_path)),
        (str(replay.workflow.human_input_path), str(original.workflow.human_input_path)),
        (str(replay.workflow.input_request_path), str(original.workflow.input_request_path)),
        (
            str(project_runtime_skills_dir(replay.state_dir).resolve()),
            source_skills_dir,
        ),
        (
            str(project_runtime_skills_dir(original.state_dir).resolve()),
            source_skills_dir,
        ),
        (
            str((replay.repo_path / ".agent-supervisor" / "skills").resolve()),
            source_skills_dir,
        ),
        (
            str((original.repo_path / ".agent-supervisor" / "skills").resolve()),
            source_skills_dir,
        ),
    ]:
        if old and old != new:
            replacements.append((old, new))
    replacements.sort(key=lambda pair: len(pair[0]), reverse=True)
    for old, new in replacements:
        text = text.replace(old, new)
    return text


def _prompt_diff(old: str, new: str, *, max_lines: int = 120) -> str:
    diff_lines = list(
        difflib.unified_diff(
            old.splitlines(),
            new.splitlines(),
            fromfile="historical",
            tofile="replay",
            lineterm="",
        )
    )
    if len(diff_lines) > max_lines:
        diff_lines = diff_lines[:max_lines] + ["... [diff truncated]"]
    return "\n".join(diff_lines)


def _load_cycle_artifacts(repo_path: Path, cycle: int) -> _CycleArtifacts:
    chats = ensure_chat_repo(repo_path)
    prefix = f"cycle-{cycle:04d}/"
    result = _git(chats, "ls-tree", "-r", "--name-only", f"cycle-{cycle}", "--", prefix, check=False)
    if result.returncode != 0:
        return _CycleArtifacts(by_exact={}, by_base={})

    prompt_artifacts: Dict[str, _ArtifactInfo] = {}
    by_base: Dict[str, List[_ArtifactInfo]] = {}
    prompt_paths = [
        line.strip() for line in result.stdout.splitlines()
        if line.strip().startswith(prefix) and line.strip().endswith("/prompt.txt")
    ]
    for prompt_path in prompt_paths:
        rel = prompt_path[len(prefix):]
        artifact_id = rel.split("/", 1)[0]
        prompt_result = _git(chats, "show", f"cycle-{cycle}:{prompt_path}", check=False)
        prompt = prompt_result.stdout if prompt_result.returncode == 0 else ""
        match = ARTIFACT_ATTEMPT_RE.fullmatch(artifact_id)
        if match:
            base = match.group("base")
            attempt_raw = match.group("attempt")
            attempt = int(attempt_raw) if attempt_raw else 1
        else:
            base = artifact_id
            attempt = 1
        info = _ArtifactInfo(exact_id=artifact_id, base=base, attempt=attempt, prompt=prompt)
        prompt_artifacts[artifact_id] = info
        by_base.setdefault(base, []).append(info)

    for items in by_base.values():
        items.sort(key=lambda item: (item.attempt, item.exact_id))
    return _CycleArtifacts(by_exact=prompt_artifacts, by_base=by_base)


def _first_attempt_artifact(cycle_artifacts: _CycleArtifacts, base: str) -> Optional[_ArtifactInfo]:
    items = cycle_artifacts.by_base.get(base, [])
    for item in items:
        if item.attempt == 1:
            return item
    return items[0] if len(items) == 1 else None


def _current_previous_outcome(history: List[Dict[str, Any]], cycle: int) -> Optional[Dict[str, Any]]:
    if cycle <= 1:
        return None
    previous = next((entry for entry in history if int(entry.get("cycle", 0) or 0) == cycle - 1), None)
    if not previous:
        return None
    result = {
        "outcome": previous.get("outcome", ""),
        "detail": previous.get("detail", ""),
    }
    if result["outcome"] == "REJECTED":
        result["rejection"] = {
            "summary": result["detail"],
            "results": previous.get("verification_results", []),
        }
    return result


